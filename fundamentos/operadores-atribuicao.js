const a = 7
let b = 3

//atribuição aditiva
//b = b + a
b += a
console.log(b)

//atribuição subtrativa
//b = b - 4
b -= 4
console.log(b)

//atribuição multiplicativa
//b = b * 2
b *= 2
console.log(b)

//atribuição divisiva
//b = b / 2
b /= 2
console.log(b)

//atribuição modular
//b = b % 2
b %= 2
